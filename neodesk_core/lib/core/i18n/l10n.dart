import 'package:flutter/foundation.dart';

/// The UI languages neodesk's own redesigned UI is translated into. (The engine
/// dialogs are localised separately via the engine's own language option.)
enum AppLocale { en, zh, ja }

/// Reactive current UI locale. The neodesk root listens to this so the whole UI
/// rebuilds when the Language setting changes; [tr] reads it.
final ValueNotifier<AppLocale> appLocale = ValueNotifier(AppLocale.en);

/// Map a stored Language setting (`system` / `en` / `zh-cn` / `ja`) to a locale.
/// `system` follows the device language, falling back to English.
AppLocale resolveLocale(String setting) {
  switch (setting) {
    case 'en':
      return AppLocale.en;
    case 'zh-cn':
      return AppLocale.zh;
    case 'ja':
      return AppLocale.ja;
    default: // 'system'
      final code = PlatformDispatcher.instance.locale.languageCode.toLowerCase();
      if (code == 'zh') return AppLocale.zh;
      if (code == 'ja') return AppLocale.ja;
      return AppLocale.en;
  }
}

/// Apply a stored Language setting to the live UI locale.
void applyLocale(String setting) => appLocale.value = resolveLocale(setting);

/// Translate the English source string [key] to the current [appLocale],
/// falling back to [key] itself when there is no translation. Using the English
/// text as the key keeps call sites readable: `tr('Settings')`.
String tr(String key) {
  final locale = appLocale.value;
  if (locale == AppLocale.en) return key;
  return _t[locale]?[key] ?? key;
}

/// `key` with `{}` replaced by [arg] (for the few parameterised strings).
String trArg(String key, Object arg) => tr(key).replaceFirst('{}', '$arg');

